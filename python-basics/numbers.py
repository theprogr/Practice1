# Example 1: Integer 
x = 5
print(x)
print(type(x))

# Example 2: Float
y = 2.75
print(y)
print(type(y))

# Example 3: Complex number
z = 3 + 4j
print(z)
print(type(z))

# Example 4: Converting numbers
a = int(3.8)
b = float(7)
print(a)
print(b)

# Example 5: Random number
import random
r = random.randrange(1, 10)
print(r)
print(type(r))
