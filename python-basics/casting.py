# Example 1: Casting to int
x = int(3.8)
print(x)
print(type(x))

# Example 2: Casting to float
y = float(5)
print(y)
print(type(y))

# Example 3: Casting to string
z = str(10)
print(z)
print(type(z))

# Example 4: Casting from string to number
a = int("7")
b = float("3.5")
print(a)
print(b)

# Example 5: Using variables with casting
num1 = "8"
num2 = 2
result = int(num1) + num2
print(result)
