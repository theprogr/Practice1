# Example 1: Integer (int)
x = 10
print(x)
print(type(x))

# Example 2: Float
y = 3.14
print(y)
print(type(y))

# Example 3: String (str)
text = "Hello, Python"
print(text)
print(type(text))

# Example 4: Boolean (bool)
is_active = True
print(is_active)
print(type(is_active))

# Example 5: List
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(type(numbers))
